select 'UPDATE LG_326_01_STFICHE SET DATE_=''' 
        + CONVERT(VARCHAR(8), LG_326_01_STFICHE.CAPIBLOCK_CREADEDDATE, 112) 
        + ''' WHERE LOGICALREF=' 
        + CAST(LG_326_01_STFICHE.LOGICALREF AS VARCHAR(20)) AS SORGU,
        DATE_, 
         LG_326_01_STFICHE.CAPIBLOCK_CREADEDDATE, 
         * from LG_326_01_STFICHE WHERE cast(DATE_ as date)<> cast(LG_326_01_STFICHE.CAPIBLOCK_CREADEDDATE as date)
		--select * INTO LG_326_01_STFICHE_20261103 from LG_326_01_STFICHE
 
SELECT 
    LG_326_01_STFICHE.CAPIBLOCK_CREADEDDATE,
    LG_326_01_STLINE.DATE_, 
    'UPDATE LG_326_01_STLINE SET DATE_=''' 
        + CONVERT(VARCHAR(8), LG_326_01_STFICHE.CAPIBLOCK_CREADEDDATE, 112) 
        + ''' WHERE LOGICALREF=' 
        + CAST(LG_326_01_STLINE.LOGICALREF AS VARCHAR(20)) AS SORGU,
    *
FROM LG_326_01_STLINE 
LEFT JOIN LG_326_01_STFICHE 
    ON LG_326_01_STFICHE.LOGICALREF = LG_326_01_STLINE.STFICHEREF
WHERE LG_326_01_STLINE.DATE_ > '20261231'
  AND CAST(LG_326_01_STFICHE.CAPIBLOCK_CREADEDDATE AS DATE) <> CAST(LG_326_01_STLINE.DATE_ AS DATE);


 