		,LG_076_21_BNFICHE.LOGICALREF
		FROM LG_076_21_CLFLINE 
		LEFT OUTER JOIN LG_076_CLCARD ON LG_076_21_CLFLINE.CLIENTREF = LG_076_CLCARD.LOGICALREF 
		LEFT OUTER JOIN LG_076_21_CLFICHE ON LG_076_21_CLFLINE.SOURCEFREF = LG_076_21_CLFICHE.LOGICALREF 
		LEFT OUTER JOIN LG_076_21_BNFICHE ON LG_076_21_CLFLINE.SOURCEFREF = LG_076_21_BNFICHE.LOGICALREF 
		LEFT OUTER JOIN LG_076_21_EMFICHE ON LG_076_21_EMFICHE.SOURCEFREF = LG_076_21_CLFLINE.SOURCEFREF
	 

	 
		WHERE  
		(LG_076_21_CLFLINE.CANCELLED <> '1') --AND LG_076_21_CLFLINE.TRCODE NOT IN (14,21,5,21,1,2)  
	 
